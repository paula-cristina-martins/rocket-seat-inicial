// 8. Crie um script que declare duas variáveis e exiba o resultado da divisão entre elas.

let numero_1 = Number(prompt("Insira o primeiro número: "))
let numero_2 = Number(prompt("Insira o segundo número: "))

let div = numero_1 / numero_2

alert(`A divisão entre ${numero_1} / ${numero_2} = ${div}`)
